package com.library.model;
import jakarta.persistence.*;
import java.io.Serializable;
import java.time.LocalDate;

@Entity @Table(name = "transactions")
public class BorrowTransaction implements Serializable {
    @Id @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    @ManyToOne @JoinColumn(name = "book_id", nullable = false) private Book book;
    @ManyToOne @JoinColumn(name = "member_id", nullable = false) private Member member;
    @Column(nullable = false) private LocalDate issueDate;
    @Column(nullable = false) private LocalDate dueDate;
    private LocalDate returnDate;
    private String status;

    public Long getId() { return id; }
    public void setId(Long id) { this.id = id; }
    public Book getBook() { return book; }
    public void setBook(Book book) { this.book = book; }
    public Member getMember() { return member; }
    public void setMember(Member member) { this.member = member; }
    public LocalDate getIssueDate() { return issueDate; }
    public void setIssueDate(LocalDate issueDate) { this.issueDate = issueDate; }
    public LocalDate getDueDate() { return dueDate; }
    public void setDueDate(LocalDate dueDate) { this.dueDate = dueDate; }
    public LocalDate getReturnDate() { return returnDate; }
    public void setReturnDate(LocalDate returnDate) { this.returnDate = returnDate; }
    public String getStatus() { return status; }
    public void setStatus(String status) { this.status = status; }
}
